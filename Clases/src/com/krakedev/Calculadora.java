package com.krakedev;

public class Calculadora {
	
	public int sumar(int num1, int num2) {
		int result;
		result = num1 + num2;
		return result;
	}
	
	public int restar(int num1, int num2) {
		int result;
		result = num1 - num2;
		return result;
	}
	
	public double multiplicar(double valor1, double valor2) {
		double result;
		result = valor1 * valor2;
		return result;
	}
	

}
